package org.example;

public class Model {
    public static int sum(int a, int b){
        return a+b;
    }
    public static int prod(int a, int b){
        return a*b;
    }
    public static int div(int a, int b){
        return a/b;
    }
    public static int sub(int a, int b){
        return a-b;
    }
}
